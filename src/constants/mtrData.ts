export const getStationName = (code: string, lang: 'en' | 'tc' = 'en') => {
    const station = MTR_STATIONS[code];
    if (!station) return code;
    return lang === 'tc' ? station.tc : station.en;
};

export const getLineColor = (line: string) => MTR_LINE_COLORS[line] || 'var(--primary-color)';
